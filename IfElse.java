public class IfElse{
    public static void main(String[] args) {
        
        int v1 = 10;
        int v2 = 20;

        if (v1 > v2) {
            System.out.println("V1 is Bigger");
        }
        else if (v2 > v1) {
            System.out.println("V2 is Bigger");
        }
        else 
            System.out.println("V1 and V2 Both Are equal..."); 
    
            
    }
}