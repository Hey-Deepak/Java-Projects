public class For
{
    public static void main(String[] args) {
        
        
        for(int val1 = 5; val1 < 100; val1 *= 2 )
        {
            System.out.print(val1);
            System.out.print("* 2 = ");
            
            System.out.println(val1);
        }
        
        System.out.println("Happy Ending.. :-)");
    }
}